function translate(en, lang) {
  if (DICT[lang][en]) return DICT[lang][en];
  return en;
}

for (const lang of ['ko', 'ja', 'es']) {
  const result = {};
  const allStrings = JSON.parse(fs.readFileSync('/tmp/i18n-strings.json', 'utf-8'));
  let translated = 0;
  for (const en of allStrings) {
    const t = translate(en, lang);
    result[en] = t;
    if (t !== en) translated++;
  }
  
  fs.writeFileSync(`/tmp/i18n-${lang}.json`, JSON.stringify(result, null, 2));
  console.log(`${lang}: ${translated}/${allStrings.length} translated (${(translated/allStrings.length*100).toFixed(0)}%)`);
}
